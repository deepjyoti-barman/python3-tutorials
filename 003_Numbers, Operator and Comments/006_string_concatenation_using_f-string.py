""" Program to demonstrate string concatenation using f-string Python3 """

# String concatenation using f-string
# From, Python 3.6+ we can use f-string for string concatenation
# Python f-string is cleaner and easier to write when compared to format() function
# It also calls str() function when an object argument is used as field replacement
# f-string can be used to concatenate any type of operand
name = "Padmini"
chocolate_amt = 4
roses_amt = 7
proposals_amt = 3

print(f"{name} got {chocolate_amt} chocolates, {roses_amt} roses and {proposals_amt} proposals on Valentine's Day")