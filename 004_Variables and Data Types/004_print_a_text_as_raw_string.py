""" Program to print a text as raw string """

# If we try to print the follwing text, it will consider '\n' in '\navin' as an escape sequence and throw a compile time error
# Hence, to cancel out the special meaning of '\n' or newline character we have to convert it into a raw string
print(r'C:\users\navin')