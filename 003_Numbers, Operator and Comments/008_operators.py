""" Program to demonstrate the arithmetic operators available in Python3 """

# There are 7 different arithmetic operators in Python3
# Addition Operator: +
print("4 + 5 =", 4 + 5)

# Subtraction Operator: -
print("7 - 10.50 =", 7 - 10.50)

# Multiplication Operator: *
print("9 * 60 =", 9 * 60)

# Division Operator: /
print("21 / 4 =", 21 / 4)

# Integer Division Operator: //
print("21 // 4 =", 21 // 4)

# Modulo Division or Remainder Operator: %
print("11 % 3 =", 11 % 3)

# Exponentiation Operator: **
print("10 ** 3 =", 10 ** 3)
print("49 ** 0.5 =", 49 ** 0.5)     # Square root of a num using exponentiation operator